          <!-- Actions de synchronisation -->
          <div class="grid grid-cols-1 md:grid-cols-2 gap-4">
            <button 
              @click="syncAll"
              :disabled="isSyncing"
              class="px-4 py-2 bg-blue-600 text-white rounded-md hover:bg-blue-700 disabled:bg-gray-400"
